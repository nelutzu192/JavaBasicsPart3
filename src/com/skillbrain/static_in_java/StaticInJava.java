package com.skillbrain.static_in_java;

public class StaticInJava {

    //variablia statica
    static String variabilaStatica = "variabila statica";

    //variabila publica
    public String variabilaPublica = "variabila publica";

    //metoda statica
    static  void metodaStatica(){
        System.out.println("metoda statica");
    }

    //metoda publica
    public void metodatPublica(){
        System.out.println("metoda publica");
    }

}
